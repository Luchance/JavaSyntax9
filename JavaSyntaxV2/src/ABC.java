public class ABC {
    public static void main(String[] args) {

        char ch;

        System.out.println("Uppercase alphabet:");
        for(ch='A';ch<='Z';ch++)
            System.out.print(ch + " ");

        System.out.println("");

        System.out.println("Lowercase alphabet:");
        for(ch='a';ch<='z';ch++)
            System.out.print(ch + " ");
    }
}
